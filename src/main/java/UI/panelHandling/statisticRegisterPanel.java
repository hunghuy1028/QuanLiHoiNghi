    private void settingUp(int i, int idConference, int status)
    {
        serialLabel.setText(String.valueOf(i));
        Conference conference = ConferenceDAO.getConference(idConference);
        ImageIcon img = new ImageIcon(new ImageIcon(getClass().getResource(conference.getHinhAnh())).getImage().getScaledInstance(150, 100, Image.SCALE_DEFAULT));
        imageLabel.setIcon(img);
        nameLabel.setText(conference.getTen());
        String date = SupportFile.getCustomTimeString(conference.getThoiGian());
        timeLabel.setText(date);
        if(status == 0)
        {
            statusLabel.setText("Pending accept");
        }
        else{
            statusLabel.setText("Accepted");
        }
        
    }

